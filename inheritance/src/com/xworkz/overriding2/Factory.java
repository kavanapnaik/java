package com.xworkz.overriding2;

public class Factory
{
        void manufacture()
        {
            System.out.println("Manufacturing in process.");
        }
        void dispose()
        {
            System.out.println("Disposing waste.");
        }
        void power()
        {
            System.out.println("Factory power is on.");
        }
        void start()
        {
            System.out.println("Factory started.");
        }
        void run()
        {
            System.out.println("Factory is running.");
        }
    }

